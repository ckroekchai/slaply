import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const promptVersion = "slaply-preflight-v0.1";

const promptFiles = [
  "prompt.md",
  "issue_library.md",
  "examples.md",
  "rubric.md",
  "disclaimers.md"
];

function readPromptFile(filename) {
  const relativePath = `slaply-ai/prompts/${filename}`;
  const absolutePath = path.join(repoRoot, relativePath);
  return {
    filename,
    relativePath,
    content: readFileSync(absolutePath, "utf8")
  };
}

function extractBlock(file, blockName) {
  const pattern = new RegExp(`<!--\\s*${blockName}:start\\s*-->([\\s\\S]*?)<!--\\s*${blockName}:end\\s*-->`);
  const match = file.content.match(pattern);
  return match ? match[1].trim() : "";
}

function compactJoin(parts) {
  return parts.map((part) => part.trim()).filter(Boolean).join("\n\n");
}

const files = promptFiles.map(readPromptFile);
const systemPromptTemplate = compactJoin(files.map((file) => extractBlock(file, "system")));
const userPromptTemplate = compactJoin(files.map((file) => extractBlock(file, "user")));

const generated = `// This file is generated by scripts/generate-ai-prompts.mjs.
// Do not edit directly. Edit slaply-ai/prompts/*.md instead.

export const PROMPT_VERSION = ${JSON.stringify(promptVersion)};

export const PROMPT_SOURCE_FILES = ${JSON.stringify(files.map((file) => file.relativePath), null, 2)};

export const SYSTEM_PROMPT_TEMPLATE = ${JSON.stringify(systemPromptTemplate)};

export const USER_PROMPT_TEMPLATE = ${JSON.stringify(userPromptTemplate)};
`;

const outputPath = path.join(repoRoot, "lib/generated/slaply-ai-prompts.generated.js");
mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(outputPath, generated);
